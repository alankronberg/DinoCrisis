void AVoxelCube::UpdateMesh()
{
	Vertices.Reset();
	Triangles.Reset();

	MarchingCubes();
	parent->UpdateMesh(section);
	
}

void AVoxelCube::MarchingCubes()
{
	int cubeIndex = 0;
	for (int i = 0; i < 8; i++) {
		if (cornerData.values[i] <= isoLevel) {
			cubeIndex += pow(2, i);
		}
	}
	if (cubeIndex != 0 && cubeIndex != 255) {
		int* tEdges = edgeTable1[cubeIndex];
		for (int i = 0; *(tEdges + i) != -1; i++) {
			if (*(tEdges + i) == 0) {
				Vertices.Add(Midpoint(cornerData.points[0], cornerData.points[1]));
			}
			else if (*(tEdges + i) == 1) {
				Vertices.Add(Midpoint(cornerData.points[1], cornerData.points[2]));
			}
			else if (*(tEdges + i) == 2) {
				Vertices.Add(Midpoint(cornerData.points[2], cornerData.points[3]));
			}
			else if (*(tEdges + i) == 3) {
				Vertices.Add(Midpoint(cornerData.points[3], cornerData.points[0]));
			}
			else if (*(tEdges + i) == 4) {
				Vertices.Add(Midpoint(cornerData.points[4], cornerData.points[5]));
			}
			else if (*(tEdges + i) == 5) {
				Vertices.Add(Midpoint(cornerData.points[5], cornerData.points[6]));
			}
			else if (*(tEdges + i) == 6) {
				Vertices.Add(Midpoint(cornerData.points[6], cornerData.points[7]));
			}
			else if (*(tEdges + i) == 7) {
				Vertices.Add(Midpoint(cornerData.points[7], cornerData.points[4]));
			}
			else if (*(tEdges + i) == 8) {
				Vertices.Add(Midpoint(cornerData.points[4], cornerData.points[0]));
			}
			else if (*(tEdges + i) == 9) {
				Vertices.Add(Midpoint(cornerData.points[5], cornerData.points[1]));
			}
			else if (*(tEdges + i) == 10) {
				Vertices.Add(Midpoint(cornerData.points[6], cornerData.points[2]));
			}
			else {
				Vertices.Add(Midpoint(cornerData.points[7], cornerData.points[3]));
			}
		}
		for (int i = 0; i < Vertices.Num(); i += 3) {
			Triangles.Add(i);
			Triangles.Add(i + 2);
			Triangles.Add(i + 1);
		}
	}


}

FVector AVoxelCube::Midpoint(FVector point1, FVector point2)
{
	FVector midPoint;
	midPoint.X = (point1.X + point2.X) / 2;
	midPoint.Y = (point1.Y + point2.Y) / 2;
	midPoint.Z = (point1.Z + point2.Z) / 2;
	return midPoint;
}

