					Vertices.Add(Midpoint(rootLoc + (FVector(cubes[i].corners[6][0], cubes[i].corners[6][1], cubes[i].corners[6][2]) * 100), rootLoc + (FVector(cubes[i].corners[2][0], cubes[i].corners[2][1], cubes[i].corners[2][2]) * 100)));
